public class Forloop_SumofnaturalNum {

        public static void main(String[] args) {
            int N = 9;
            int sum = 0;
            int i = 0;

            for ( ;N >= i; )
            {
                sum = sum+i;
                i++;
            }
            System.out.println("Sum of N Number is : " + sum );
        }

}
